        <h1>Environment Issue</h1>
        
        <p>Environmental issues are harmful aspects of human activity on the biophysical environment. Environmentalism, a social and environmental movement that started in the 1960s, addresses environmental issues through advocacy, education and activism. current problems faced by the environment- The carbon dioxide equivalent of greenhouse gases in the atmosphere has already exceeded 400 parts per million. This level is considered a tipping point. "The amount of greenhouse gas in the atmosphere is already above the threshold that can potentially cause dangerous climate change. We are already at risk...It's not next year or next decade, it's now." Report from the UN Office for the Coordination of Humanitarian Affairs: "Climate disasters are on the rise. Around 70 percent of disasters are now climate related – up from around 50 percent from two decades ago. These disasters take a heavier human toll and come with a higher price tag. In the last decade, 2.4 billion people were affected by climate related disasters, compared to1.7 billion in the previous decade. The cost of responding to disasters has risen tenfold between 1992 and 2008. Destructive sudden heavy rains, intense tropical storms, repeated flooding and droughts are likely to increase, as will the vulnerability of local communities in the absence of strong concerted action." "Climate change is not just a distant future threat. It is the main driver behind rising humanitarian needs and we are seeing its impact. The number of people affected and the damages inflicted by extreme weather has been unprecedented."</p>

        <br><h1>These are the five types of the world's biggest environmental problems</h1>

        <p>These five megatrends present major global threats for planet Earth — problems that must be solved if the world is to remain a supportive habitat for humans and other species. We look at the causes and possible solutions.</p><br>

        <h2>- Air pollution and climate change</h2><br>

        <p>Overloading of the atmosphere and of ocean waters with carbon. Atmospheric CO2 absorbs and re-emits infrared-wavelength radiation, leading to warmer air, soils, and ocean surface waters - which is good: The planet would be frozen solid without this.</p>

        <p>Unfortunately, there's now too much carbon in the air. Burning of fossil fuels, deforestation for agriculture, and industrial activities have pushed up atmospheric CO2 concentrations from 280 parts per million (ppm) 200 years ago, to about 400 ppm today. That's an unprecedented rise, in both size and speed. The result: climate disruption.</p>
            
        <p>Carbon overloading is only one form of air pollution caused by burning coal, oil, gas and wood. The World Health Organization recently estimated that one in nine deaths in 2012 were attributable to diseases caused by carcinogens and other poisons in polluted air.</p>
            
        <p>Solutions: Replace fossil fuels with renewable energy. Reforestation. Reduce emissions from agriculture. Change industrial processes.</p>
            
        <p>The good news is that clean energy is abundant - it just needs to be harvested. Many say a 100 percent renewable-energy future is feasible with existing technology now.</p>
            
        <p>But the bad news is that even though renewable energy infrastructure - solar panels, wind turbines, energy storage and distribution systems - are already widespread, and getting cheaper and more efficient all the time, experts say we're not applying them quickly enough to prevent catastrophic climate disruption. Barriers in policy and finance remain to be overcome.</p>

        <img src="images/image23.jpg" alt="">

        <h2>- Deforestation</h2><br>

        <p>Species-rich wild forests are being destroyed, especially in the tropics, often to make way for cattle ranching, soybean or palm oil plantations, or other agricultural monocultures.</p>

        <p>Today, about 30 percent of the planet's land area is covered by forests - which is about half as much as before agriculture got started around 11,000 years ago. About 7.3 million hectares (18 million acres) of forest are destroyed each year, mostly in the tropics. Tropical forests used to cover about 15 percent of the planet's land area; they're now down to 6 or 7 percent. Much of this remainder has been degraded by  logging or burning.</p>
            
        <p>Not only do natural forests act as biodiversity reserves, they are also carbon sinks, keeping carbon out of the atmosphere and oceans.</p>
            
        <p>Solutions: Conserve of what's left of natural forests, and restore degraded areas by replanting with native tree species. This requires strong governance - but many tropical countries are still developing, with increasing populations, uneven rule-of-law, and widespread cronyism and bribery when it comes to allocating land use.</p>
            
        <img src="images/image27.jpg" alt="">

        <h2>- Species extinction</h2><br>

        <p>On land, wild animals are being hunted to extinction for bushmeat, ivory, or "medicinal" products. At sea, huge industrial fishing boats equipped with bottom-trawling or purse-seine nets clean out entire fish populations. The loss and destruction of habitat are also major factors contributing to a wave of extinction - unprecedented in that it is caused by a single species: humans. The IUCN's Red List of threatened and endangered species continues to grow.</p>

        <p>Not only do species inherently deserve to exist, they also provide products and "services" essential to human survival. Think bees and their pollinating prowess - necessary for growing food.</p>
            
        <p>Solutions: Concerted efforts need to be made to prevent further loss of biodiversity. Protecting and restoring habitats is one side of this - protecting against poaching and wildlife trade is another. This should be done in partnership with locals, so that wildlife conservation is in their social and economic interest.</p>
            
        <img src="images/image25.jpg" alt="">    

        <h2>- Soil degradation</h2><br>

        <p>Overgrazing, monoculture planting, erosion, soil compaction, overexposure to pollutants, land-use conversion - there's a long list of ways that soils are being damaged. About 12 million hectares of farmland a year get seriously degraded, according to UN estimates.</p>

        <p>Solutions: A wide range of soil conservation and restoration techniques exist, from no-till agriculture to crop rotation to water-retention through terrace-building. Given that food security depends on keeping soils in good condition, we're likely master this challenge in the long run. Whether this will be done in a way equitable to all people around the globe, remains an open question.</p>
            
        <img src="images/image26.jpg" alt="">  

        <h2>- Overpopulation</h2><br>

        <p>Human population continues to grow rapidly worldwide. Humanity entered the 20th century with 1.6 billion people; right now, we're about 7.5 billion. Estimates put us at nearly 10 billion by 2050. Growing global populations, combined with growing affluence, is putting ever greater pressure on essential natural resources, like water. Most of the growth is happening on the African continent, and in southern and eastern Asia.</p>

        <p>Solutions: Experience has shown that when women are empowered to control their own reproduction, and gain access to education and basic social services, the average number of births per woman drops precipitously.</p>
          
        <p>Done right, networked aid systems could bring women out of extreme poverty, even in countries where state-level governance remains abysmal.</p>


        <br><br><br><h2>5. Eat sustainably.</h2>

	    <p><br>Food production is a major driver of wildlife extinction. What we eat contributes around a quarter of global greenhouse gas emissions and is responsible for almost 60% of global biodiversity loss.</p>
		
        <p>Farming animals for meat and dairy requires space and huge inputs of water and feed. Today, one of the biggest causes of forest loss is the expansion of agricultural land for animal feed production, such as soy. And producing meat creates vastly more carbon dioxide than plants such as vegetables, grains and legumes.</p>

        <p>Moving away from a meat-dominated diet towards a more plant-based diet can lower your impact on the environment. Vegetarian and vegan foods are massively on the rise and becoming far more common in restaurants, cafes and supermarkets, so you’ll rarely struggle.</p>
        
        <p>Not only that, but cutting down on meat and dairy products can reduce your weekly food bills.</p><br>

        <img src="images/image30.png" alt=""><br>

        <br><br><br><h2>6. Plant Tress.</h2>

	    <p><br>In 2018 the United Nations' Intergovernmental Panel on Climate Change (IPCC) report, the U.N. suggests an additional 2.5 billion acres (1 billion hectares) of forest in the world could limit global warming to 2.7 degrees Fahrenheit (1.5 degrees Celsius) by 2050. That's a lot of trees, but you could plant one or two, right?</p>
		
        <p>One young tree can absorb CO2 at a rate of 13 pounds (5 kilograms) per tree. Every. Single. Year. And that's just an itty bitty baby tree. Once that tree reaches about 10 years old, it's at its most productive stage of carbon storage. Then it can absorb 48 pounds (21 kilograms) of CO2 per year. </p>

        <p>Trees also remove all other kinds of junk from the air, including sulfur dioxide, nitrogen oxides and small particles. So go ahead, plant a tree. It's good for everybody.</p><br>


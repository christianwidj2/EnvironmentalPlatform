        <p>The scale of the challenges facing our planet can seem daunting, but we can all do something. Here are 10 simple ways you can help reduce your impact, and help in the fight against climate change.</p><br>
        
        <h2>1. Use your Voice.</h2>

	    <p><br>We are the first generation to know we’re destroying the world, and we could be the last that can do anything about it. Speaking up is one of the most powerful things you can do especially if it’s to the right people. Talk to your MP. Tell them to commit to action to protect our natural world. <br><br>Contact the brands you buy from and get them to tell you how their products are sourced. Use social media – this is one of the most effective ways to get brands to listen to you, so tell them that you want a change.<br><br>It’s not just about speaking to the people in charge. Talk to your friends, neighbours and colleagues and get them to make positive changes too. Speak up, speak to everyone, and make your voice heard.</p><br>
        
        <h2>2. Be informed.</h2>

	    <p><br>One of the best things you can do is to keep yourself informed – the more you know the better. It leaves you better equipped to have those conversations with your friends and family and the people you want to influence. Get yourself clued up on the facts, stay up to date with recent news on the state of our natural world and work out what you can do. </p>
		
        <p>We have the world at our fingertips, so learn from influential people, keep up with the news and research organisations that are working to make our planet a better place.</p><br>

        <h2>3. Be political.</h2>

	    <p><br>Everyone in the UK over the age of 18 can vote for their MP. This is an opportunity to vote for someone who is representative of you and your views and will make the environment a top priority.</p>
		
        <p>Being politically engaged is not limited to voting and it certainly isn’t limited by how young you are. Every year more and more young people are working together to show our political leaders that they want change.</p>

        <p>We need to ensure we hold our politicians accountable. You can do that by contacting your local MP or attending constituency meetings where you will have an opportunity to make your voice heard.</p><br>

        <h2>4. Reduce your Waste.</h2>

	    <p><br>We need to make wasting our resources unacceptable in all aspects of our life.  Every product we buy has an environmental footprint and could end up in landfill. The impact of plastic pollution on our oceans is becoming increasingly clear, having drastic impacts on marine life.</p>
		
        <p>Recycling what we can reduces the amount of new materials we are making, and upcycling is a creative way to make old items into something more valuable. This could be reusing a jam jar as a candle holder, or using old tins as plant pots – the possibilities are endless!</p>

        <p>It’s not just the products we buy. It’s estimated that a third of all food produced in the world is lost or wasted. Do your bit by eating up leftovers and use any ingredients you have spare to make interesting meals. Try to waste as little food as possible, and compost the organic waste you can’t eat.</p><br>

